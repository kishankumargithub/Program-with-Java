public class Multidimensional {
    public static void main(String[] args) {
        int [][] arr={{1,2,3},{3,5,6},{7,4,7}};
        for(int i=0; i<3; i++){
            for(int j=0; j<3; j++){
                System.out.println(arr[i][j] +"");
            }
System.out.println();
        }
    }
    
}
